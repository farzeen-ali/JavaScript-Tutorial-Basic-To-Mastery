/******************** Object Oriented Programming (OOP) *********************
One of the most common strategies in programming is to solve an issue by creating objects.

DRY------> Don't Repeat Yourself

************** Object *****************
Anything in the world (living and non-living thing)
An object is a member also known as instance of a class.
Each Object has an:
identity, behavior(actions), and a state(features / properties / attributes).

************** Class *****************
Blueprint to create objects.
contains information to create a valid objects


state of object ----> variables
behavior of object ----> methods (functions)

************** How To Model a Problem In OOPs **************
Identity----> Class -----> Employee
State-------> Attributes ----> empName, empAge, empSalary;
Behavior----> Methods -----> getSalary();
*/
